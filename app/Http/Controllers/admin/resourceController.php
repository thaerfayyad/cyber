        $validator = Validator::make($request->all(), [
            'name' => 'required',
            'details' => 'required',
            'file'   => 'mimes:doc,pdf,docx,zip'
        ]);
        $book = new Book();
        $book->name = $request->name;
        $book->details = $request->details ;
        $book->author = $request->author ;
        $book->file = $request->file ;
        $book->save();

        return redirect()->back();
    }

